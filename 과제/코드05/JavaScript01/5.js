// 값 100을 저장한 변수 number1 생성
let number1 = 100;

// 값 200을 저장한 변수 number2 생성
let number2 = 200;

// 사칙연산 결과 출력
console.log('덧셈:', number1 + number2); // 출력: 300
console.log('뺄셈:', number1 - number2); // 출력: -100
console.log('곱셈:', number1 * number2); // 출력: 20000
console.log('나눗셈:', number1 / number2); // 출력: 0.5
